    print('userAddress: $userAddress');
    print('uid: $uid');
    print('amount: $amount');

    final function = _contract.function('registerReward');
    final result = await _client.sendTransaction(
      _credentials,
      Transaction.callContract(
        contract: _contract,
        function: function,
        parameters: [
          EthereumAddress.fromHex(userAddress),
          uid,
          BigInt.from(amount)
        ],
      ),
      chainId: null, // A√±ade el ID de la cadena si es necesario
      fetchChainIdFromNetworkId: true,
    );
    return result;
  }
