  Future<double> getBalance(String userAddress) async {
    final function = _contract.function('balanceOf');
    final result = await _client.call(
      contract: _contract,
      function: function,
      params: [EthereumAddress.fromHex(userAddress)],
    );
    /// divide by 10^18 to get the actual balance
    var balance = result.first.toDouble() / 1000000000000000000;
    return balance; // Convierte el resultado a double
  }

  Future<String> registerReward(String userAddress, String uid, int amount) async {

